    for (int i=0; i<100000; i++) {
        for (map<int, int>::iterator it = triples[i].begin(); it != triples[i].end(); ++it) {
            int p = i;
            int q = it->first;
            int c = it->second;
            
            if (p + q >= N) break;
            
            for (map<int, int>::iterator it = triples[q].begin(); it != triples[q].end(); ++it) {
                int r = it->first;
                int a = it->second;
                
                if (triples[p].find(r) != triples[p].end()) {
                    int b = triples[p][r];
                    
                    if (p + q + r <= n) {
                        ans.push_back({a, b, c});
                        sort(ans.back().begin(), ans.back().end());
                        break;
                    }
                }
            }
        }
    }
    
    for (int i=0; i<pre.size(); i++)
        if (pre[i][0] <= n) ans.push_back({pre[i][1], pre[i][2], pre[i][3]});
    
