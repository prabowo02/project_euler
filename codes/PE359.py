    for n in range(1, 200):
        found = False
        for rooms in P:
            if math.isqrt(rooms[-1] + n)**2 == rooms[-1] + n:
                found = True
                rooms.append(n)

        if not found:
            P.append([n])

    for rooms in P:
        print([rooms[i] - rooms[i - 1] for i in range(1, len(rooms))])

