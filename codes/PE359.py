# Calculated based on `diff` patterns
def P(f, r):
    # a + (a + d) + ... + (a + d*(n-1))
    def arith(a, d, n):
        return (a + a + d * (n - 1)) * n // 2

    if f == 1:
        return arith(1, 1, r)

    res = arith(2, 2, f//2) * 2
    if f % 2 == 0:
        res -= f

        res += arith(f*2 + 1, 2, r//2)
        res += arith(2, 2, (r-1)//2)
        return res

    res += arith(1, 2, r//2)
    res += arith(f*2, 2, (r-1)//2)
    return res


