import fractions

N = 176
LN = 10
MOD = 10**9 + 7

half = [fractions.Fraction(1, 2**i) for i in range(LN)]
inv = [fractions.Fraction(2**(i+1), 2**i + 1) for i in range(LN)]

dp = [[None for j in range(N)] for i in range(N)]
def f(x, y):
    if y <= 0:
        return 1
    
    if x <= 0:
        return 0
        
    if dp[x][y] is not None:
        return dp[x][y]
        
    dp[x][y] = max([(
        half[1] * half[t] * f(x-1, y - 2**(t-1)) +
        half[1] * (1 - half[t]) * f(x-1, y) +
        half[1] * half[t] * f(x, y - 2**(t-1))
    ) * inv[t] for t in range(1, LN)]);
    
    return dp[x][y]


def solve(n):
    result = half[1] * (f(n, n) + f(n-1, n))
    result = result.numerator * pow(result.denominator, MOD-2, MOD) % MOD
    return result


